package com.cidesign.jianghomestyle.entity;

public class PanelEntity
{
	private int top;
	private int right;
	private int bottom;
	private int left;
	public int getTop()
	{
		return top;
	}
	public void setTop(int top)
	{
		this.top = top;
	}
	public int getRight()
	{
		return right;
	}
	public void setRight(int right)
	{
		this.right = right;
	}
	public int getBottom()
	{
		return bottom;
	}
	public void setBottom(int bottom)
	{
		this.bottom = bottom;
	}
	public int getLeft()
	{
		return left;
	}
	public void setLeft(int left)
	{
		this.left = left;
	}
	
}
